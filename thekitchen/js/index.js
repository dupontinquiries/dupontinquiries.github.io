function shuffle(arr) {
    for (var i = 1; i < arr.length; i++) {
      // get random index
      var j = i + Math.floor(Math.random() * (arr.length - i));
      // shuffle
      var temp = arr[j];
      arr[j] = arr[i];
      arr[i] = temp;
      //console.log(i + ", " + j);
    }
    return arr;
}

